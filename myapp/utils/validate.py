from myapp.base.constant import MyBaseEnum


class PatternRule(str, MyBaseEnum):
    PHONE = "^13[\d]{9}$|^14[5,7]{1}\d{8}$|^15[^4]{1}\d{8}$|^16[\d]{9}$|^17[0,2,3,5,6,7,8]{1}\d{8}$|^18[\d]{9}$|^19[\d]{9}$"  # 中国手机号
    PHONE_LOOSE = "^1(3|4|5|6|7|8|9)\d{9}$"  # 宽松的手机号
    AMERICA_PHONE = "^(00){0,1}(1){1}\d{10,12}$"  # 美国手机号
    TELEPHONE = "^((\d{3,4})|\d{3,4}-)?\d{7,8}$"  # 电话
    QQ = "^[1-9][0-9]{4,}$"  # qq号
    POSTCODE = "^\d{6}$"  # 邮编
    EMAIL = "^[a-zA-Z0-9_\-\.]+@[a-zA-Z0-9_\-]+(\.[a-zA-Z0-9_\-]+)+$"  # 邮件
    URL = ("(https?|ftp|file)://[-A-Za-z0-9+&@#/%?=~_|!:,.;]+[-A-Za-z0-9+&@#/%=~_|]"  # 网址
    )
    DOMAIN = "([0-9a-zA-Z][0-9a-zA-Z\-]{0,62}\.)+([a-zA-Z]{0,62})$"  # 域名
    IPV4 = "^((25[0-5]|2[0-4]\d|[01]?\d\d?)\.){3}(25[0-5]|2[0-4]\d|[01]?\d\d?)$"  # ipv4
    IPV6 = ("^([\da-fA-F]{1,4}:){7}[\da-fA-F]{1,4}$|^:((:[\da-fA-F]{1,4}){1,6}|:)$|^[\da-fA-F]{1,4}:((:[\da-fA-F]{1,4})"
            "{1,5}|:)$|^([\da-fA-F]{1,4}:){2}((:[\da-fA-F]{1,4}){1,4}|:)$|^([\da-fA-F]{1,4}:){3}((:[\da-fA-F]{1,4})"
            "{1,3}|:)$|^([\da-fA-F]{1,4}:){4}((:[\da-fA-F]{1,4}){1,2}|:)$|^([\da-fA-F]{1,4}:){5}:([\da-fA-F]{1,4})?$|^([\da-fA-F]{1,4}:){6}:$")  # ipv6
    MAC = "^([0-9A-Fa-f]{2}[\-:]){5}[0-9A-Fa-f]{2}$"  # 硬件mac地址
    IDCARD18 = ("(^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$)|"
                "(^[1-9]\d{5}\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}$)")  # 18位身份证
    IDCARD15 = "^[1-9]\d{5}\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}$"  # 15位身份证, 目前应该全都是18位
    EMAIL_SUFFIX = "^@[a-zA-Z0-9_\-]+(\.[a-zA-Z0-9_\-]+)+$"
